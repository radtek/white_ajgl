package com.taiji.pubsec.ajqlc.agt.action.bean;

public class MessageAlertBean {
	/**
	 * 预警内容
	 */
	private String messageAlertContent;
	/**
	 * 预警时间
	 */
	private String messageAlertTime;
	
	
	public String getMessageAlertContent() {
		return messageAlertContent;
	}
	public void setMessageAlertContent(String messageAlertContent) {
		this.messageAlertContent = messageAlertContent;
	}
	public String getMessageAlertTime() {
		return messageAlertTime;
	}
	public void setMessageAlertTime(String messageAlertTime) {
		this.messageAlertTime = messageAlertTime;
	}

}
